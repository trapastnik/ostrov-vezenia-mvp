def parse_excel(filepath: str) -> list[dict]:
    """Прочитать Excel файл с кодами ТН ВЭД (требует openpyxl).

    Поддерживает два формата:
    1. Простой: Код | Наименование | Ед.изм. | Примечание
    2. TWS.BY: Код | Наименование | Тариф | Подробности (данные на второй вкладке)
    """
    try:
        from openpyxl import load_workbook
    except ImportError:
        print("Для импорта Excel установите: pip install openpyxl")
        sys.exit(1)

    wb = load_workbook(filepath, read_only=True, data_only=True)

    # Определяем формат: если есть вкладка "ТНВЭД" — это TWS.BY
    is_tws = "ТНВЭД" in wb.sheetnames
    if is_tws:
        ws = wb["ТНВЭД"]
        print("  Формат: TWS.BY (вкладка ТНВЭД)")
    else:
        ws = wb.active

    records = []
    raw_names: dict[str, str] = {}  # code -> raw name (до clean_tws_name)
    seen_groups = set()  # Для генерации отсутствующих групп level=2

    for row in ws.iter_rows(min_row=1, values_only=True):
        if not row or not row[0]:
            continue

        code = str(row[0]).strip().replace(" ", "").replace(".", "")
        if not code or not code[0].isdigit():
            continue

        code = code.ljust(10, "0")[:10]

        if is_tws:
            raw_name = str(row[1]).strip() if len(row) > 1 and row[1] else ""
            raw_names[code] = raw_name
            name = clean_tws_name(raw_name)
            tariff = str(row[2]).strip() if len(row) > 2 and row[2] else None
            # Сохраняем тариф в note
            note = tariff if tariff and tariff != "подробнее" else None
            unit = None
        else:
            name = str(row[1]).strip() if len(row) > 1 and row[1] else ""
            unit = str(row[2]).strip() if len(row) > 2 and row[2] else None
            note = str(row[3]).strip() if len(row) > 3 and row[3] else None

        if not name:
            continue

        # Запоминаем 2-значные группы для автогенерации
        group_code = code[:2]
        seen_groups.add(group_code)

        records.append({
            "code": code,
            "name": name,
            "level": determine_level(code),
            "parent_code": determine_parent(code),
            "unit": unit if not is_tws else None,
            "note": note,
        })

    wb.close()

    # Автогенерация отсутствующих промежуточных уровней для дерева
    existing_codes = {r["code"] for r in records}
    generated_records = []
    generated_groups = 0

    # 1) Группы level=2
    for group in sorted(seen_groups):
        group_code = group.ljust(10, "0")
        if group_code not in existing_codes:
            group_name = GROUP_NAMES.get(group, f"Группа {group}")
            generated_records.append({
                "code": group_code,
                "name": group_name,
                "level": 2,
                "parent_code": None,
                "unit": None,
                "note": None,
            })
            existing_codes.add(group_code)
            generated_groups += 1

    # 2) Промежуточные уровни (4, 6, 8) — генерируем, если отсутствуют
    #    Для имени: берём первую часть raw-имени первого дочернего (до стрелки 🠺)
    children_by_parent: dict[str, str] = {}  # parent_code -> first child raw_name
    for r in records:
        pc = r.get("parent_code")
        if pc and pc not in children_by_parent:
            # Используем raw name если доступен, иначе clean name
            children_by_parent[pc] = raw_names.get(r["code"], r["name"])

    generated_intermediate = 0
    for parent_code, child_raw_name in sorted(children_by_parent.items()):
        if parent_code not in existing_codes:
            level = determine_level(parent_code)
            # Для имени: берём первую часть (до стрелки), обрезаем и капитализируем
            if "🠺" in child_raw_name:
                # "Водонепроницаемая обувь...: 🠺 прочие" → "Водонепроницаемая обувь..."
                name = child_raw_name.split("🠺")[0].rstrip(": ").strip()
            else:
                # Обрезаем до разумной длины для промежуточного узла
                name = child_raw_name.split(",")[0].strip() if "," in child_raw_name else child_raw_name
            if name:
                name = name[0].upper() + name[1:]
            generated_records.append({
                "code": parent_code,
                "name": name or f"Код {parent_code.rstrip('0')}",
                "level": level,
                "parent_code": determine_parent(parent_code),
                "unit": None,
                "note": None,
            })
            existing_codes.add(parent_code)
            generated_intermediate += 1

    if generated_groups:
        print(f"  Сгенерировано {generated_groups} корневых групп (level=2)")
    if generated_intermediate:
        print(f"  Сгенерировано {generated_intermediate} промежуточных узлов дерева")

    records = generated_records + records
    return records


async def import_records(records: list[dict]) -> int:
    """Импортировать записи в БД."""
    engine = create_async_engine(settings.DATABASE_URL, echo=False)

    # Создать таблицу если не существует
    async with engine.begin() as conn:
        await conn.run_sync(Base.metadata.create_all)

    async_session_factory = sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with async_session_factory() as session:
        # Очистить старые данные
        await session.execute(text("DELETE FROM tn_ved_codes"))

        # Batch insert
        batch_size = 500
        imported = 0
        seen_codes = set()

        for i in range(0, len(records), batch_size):
            batch = records[i : i + batch_size]
            for record in batch:
                # Дедупликация
                if record["code"] in seen_codes:
                    continue
                seen_codes.add(record["code"])

                obj = TnVedCode(**record)
                session.add(obj)
                imported += 1

            await session.flush()
            print(f"  ... {imported} записей")

        await session.commit()

    await engine.dispose()
    return imported


